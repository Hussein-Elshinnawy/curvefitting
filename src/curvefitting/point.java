package curvefitting;

public class point {
    public point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    double y;

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }



    double x;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

}
